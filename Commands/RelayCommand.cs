using System;
using System.Windows.Input;

namespace HumanResources.Commands
{

    // może się ona dowolnie nazywać, przeważnie się nazywa RelayCommand lub DelegateCommand
    // musi implementować interfejs ICommand
    // delegat przekazany jako pierwszy to metoda jaka ma być wykonana
    // delegat przekazany jako drugi - czy dana metoda może być wykonana
    
    public class RelayCommand : ICommand
    {
        readonly Action<object> _execute;
        readonly Predicate<object> _canExecute;

        public RelayCommand(Action<object> execute)
            : this(execute, null)
        {
        }

        public RelayCommand(Action<object> execute, Predicate<object> canExecute)
        {
            _execute = execute ?? throw new ArgumentNullException("execute");
            _canExecute = canExecute;
        }

        public bool CanExecute(object parameter)
        {
            return _canExecute == null || _canExecute(parameter);
        }

        public event EventHandler CanExecuteChanged
        {
            add
            {
                CommandManager.RequerySuggested += value;
            }
            remove
            {
                CommandManager.RequerySuggested -= value;
            }
        }

        public void Execute(object parameter)
        {
            _execute(parameter);
        }
    }
}